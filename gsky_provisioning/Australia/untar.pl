#!/bin/env perl
sub untar
{
	`tar xf 100/*.gz`;
	`tar xf 1000/*.gz`;
	`tar xf 20/*.gz`;
	`tar xf 200/*.gz`;
	`tar xf 300/*.gz`;
	`tar xf 3000/*.gz`;
	`tar xf 50/*.gz`;
	`tar xf 500/*.gz`;
}
@dates = ("2015-12-27 ","2016-01-06","2016-02-20","2016-04-11","2016-06-16","2016-08-04","2016-09-25","2016-10-05","2016-12-02","2016-12-08","2017-01-20","2017-03-27","2017-05-16","2017-06-30","2017-07-11","2017-08-22","2017-08-28","2017-09-03","2017-09-09","2017-10-23","2017-10-29","2017-11-02","2017-11-08","2018-02-11");

foreach $date (@dates)
{
	print "$date\n";
	chdir($date);
	&untar;
	chdir("..");
	exit;
}
